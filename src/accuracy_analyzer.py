def extract_angles_at_kinovea_times():
    """
    Extract wrist flexion angles at EXACT Kinovea time points.
    Since Kinovea times are exactly 0.033s apart (30 FPS),
    we can match them directly to your frame data.
    """
    
    print("="*60)
    print("EXTRACTING ANGLES AT KINOVEA TIMES")
    print(f"Kinovea timestamps: {len(kinovea_times)} points")
    print(f"Wrist data frames: 577 frames")
    print("="*60)
    
    # Parse wrist data
    wrist_frames = []
    lines = wrist_data.strip().split('\n')
    
    for line in lines:
        parts = line.strip().split()
        if len(parts) >= 2:
            try:
                frame = int(parts[0])
                angle = float(parts[1])
                wrist_frames.append((frame, angle))
            except ValueError:
                continue
    
    print(f"✅ Parsed {len(wrist_frames)} wrist data points")
    
    # Calculate FPS from Kinovea times
    # Time difference between consecutive Kinovea points
    time_diff = kinovea_times[1] - kinovea_times[0]
    kinovea_fps = 1 / time_diff
    
    print(f"📊 Kinovea FPS: {kinovea_fps:.2f} (Time step: {time_diff:.3f}s)")
    
    # Match Kinovea times to wrist data
    matched_data = []
    
    for kinovea_time in kinovea_times:
        # Calculate which frame corresponds to this time
        # Since Kinovea times start at 0.000s and increment by 0.033s
        # Frame = Time × FPS
        estimated_frame = int(round(kinovea_time * kinovea_fps))
        
        # Find the wrist data for this frame
        wrist_angle = None
        for frame, angle in wrist_frames:
            if frame == estimated_frame:
                wrist_angle = angle
                break
        
        if wrist_angle is not None:
            matched_data.append({
                'kinovea_time': kinovea_time,
                'frame': estimated_frame,
                'wrist_angle': wrist_angle
            })
        else:
            # If exact frame not found, use nearest frame
            print(f"⚠️  No data for Kinovea time {kinovea_time:.3f}s (frame {estimated_frame})")
    
    print(f"\n✅ Matched {len(matched_data)} out of {len(kinovea_times)} Kinovea timestamps")
    
    return matched_data

def save_matched_data(matched_data):
    """Save the matched data to CSV."""
    
    output_file = "kinovea_wrist_flexion_matched.csv"
    
    with open(output_file, 'w', newline='') as f:
        writer = csv.writer(f)
        
        # Write header
        writer.writerow(['Kinovea_Time(s)', 'Frame', 'Wrist_Flexion_Angle(deg)'])
        
        # Write all matched data
        for point in matched_data:
            writer.writerow([
                f"{point['kinovea_time']:.3f}",
                point['frame'],
                f"{point['wrist_angle']:.2f}"
            ])
    
    print(f"\n📁 Saved matched data to: {output_file}")
    print(f"📊 Total rows: {len(matched_data)}")
    
    return output_file

def calculate_statistics(matched_data):
    """Calculate statistics for the matched data."""
    
    angles = [point['wrist_angle'] for point in matched_data]
    
    if not angles:
        print("❌ No angles to calculate statistics")
        return
    
    min_angle = min(angles)
    max_angle = max(angles)
    rom = max_angle - min_angle
    avg_angle = sum(angles) / len(angles)
    
    print("\n📊 STATISTICS FOR MATCHED DATA:")
    print("="*40)
    print(f"Data points: {len(angles)}")
    print(f"Time range: {matched_data[0]['kinovea_time']:.3f}s to {matched_data[-1]['kinovea_time']:.3f}s")
    print(f"Minimum angle: {min_angle:.2f}°")
    print(f"Maximum angle: {max_angle:.2f}°")
    print(f"Range of Motion (ROM): {rom:.2f}°")
    print(f"Average angle: {avg_angle:.2f}°")
    print("="*40)
    
    # Find time points for min and max
    min_time = next(p['kinovea_time'] for p in matched_data if p['wrist_angle'] == min_angle)
    max_time = next(p['kinovea_time'] for p in matched_data if p['wrist_angle'] == max_angle)
    
    print(f"📈 Minimum angle occurs at: {min_time:.3f}s")
    print(f"📈 Maximum angle occurs at: {max_time:.3f}s")

